package testng;

public class Calculator {

    public double sum(double a, double b) {
        return a + b;
    }

    public double min(double a, double b) {
        return a - b;
    }

    public double div(double a, double b) {
        return a / b;
    }

    public double mul(double a, double b) {
        return a * b;
    }
}
